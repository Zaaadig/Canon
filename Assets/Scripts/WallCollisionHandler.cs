using System.Collections;
using UnityEngine;

public class WallCollisionHandler : MonoBehaviour
{
    [SerializeField] private PikminController m_pikminShoot;
    [SerializeField] private GameObject m_pikmin;
    private void OnTriggerEnter(Collider other)
    {
        if (other.gameObject.tag == ("Pikmin") && m_pikminShoot.IsShoot == true)
        {            
            m_pikminShoot.m_wantedPos = other.transform.position;
            print("test" + other.transform.position);
            StartCoroutine(WallHit());
        }
    }
    
    private IEnumerator WallHit()
    {
        m_pikminShoot.enabled = false;
        yield return new WaitForEndOfFrame();
        m_pikminShoot.enabled = true;
    }
}
